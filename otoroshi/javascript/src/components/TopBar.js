function extractEnv(value = '') {
  const parts = value.split(' ');
  const env = last(parts.filter((i) => i.startsWith(':')));
  const finalValue = parts.filter((i) => !i.startsWith(':')).join(' ');
  if (env) {
    return [env.replace(':', ''), finalValue];
  } else {
    return [null, value];
  }
}


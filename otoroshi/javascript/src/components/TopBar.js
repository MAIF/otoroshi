export class FlagsForm extends Component {
  state = {
    flags: {},
  };

  read = () => {
    fetch('/bo/api/backoffice/flags', {
      method: 'GET',
      credentials: 'include',
      headers: {
        Accepts: 'application/json',
      },
    })
      .then((r) => r.json())
      .then((flags) => {
        this.setState({ flags });
      });
  };

  write = () => {
    fetch('/bo/api/backoffice/flags', {
      method: 'PUT',
      credentials: 'include',
      headers: {
        Accepts: 'application/json',
        'Content-Type': 'application/json',
      },
      body: JSON.stringify(this.state.flags),
    })
      .then((r) => r.json())
      .then((flags) => {
        this.setState({ flags });
      });
  };

  componentDidMount() {
    this.read();
  }

  render() {
    return (
      <>
        <div className="modal-body">
          <form className="form-horizontal" style={{ overflowY: 'auto', maxHeight: '80vh' }}>
            <JsonObjectAsCodeInput
              hideLabel
              label="-"
              mode="json"
              value={this.state.flags || {}}
              onChange={(e) => this.setState({ flags: e })}
              example={{
                useAkkaHttpClient: false,
                logUrl: false,
                logStats: false,
                requestTimeout: 60000,
              }}
            />
          </form>
        </div>
        <div className="modal-footer">
          <button type="button" className="btn btn-primary" onClick={this.write}>
            Write
          </button>
          <button
            type="button"
            className="btn btn-success"
            ref={(r) => (this.okRef = r)}
            onClick={(e) => this.props.ok(this.state)}
          >
            Close
          </button>
        </div>
      </>
    );
  }
}

